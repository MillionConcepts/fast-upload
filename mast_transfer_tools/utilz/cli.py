"""Helper functions for implementing a command line interface.

Functions in this file named 'require_<thing>' check preconditions
which, if violated, indicate user error (some variation on the theme
of having mistyped a command).  They either silently succeed, or
print a human-readable error message and exit the program.

Functions in this file named 'fatal_<thing>' report various kinds
of fatal errors.  They always exit the program.
"""

